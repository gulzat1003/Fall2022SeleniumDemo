package seleniumHomeWorks;

public class AidaiUser {
    public String name;
    public String lastName;
    public String email;
    public int age;
    public int salary;
    public String department;

    public AidaiUser(String name, String lastName, String email, int age, int salary, String department) {
        this.name = name;
        this.lastName = lastName;
        this.email = email;
        this.age = age;
        this.salary = salary;
        this.department = department;
    }
}
